package operators;

public class ArithmeticAssignment {

	public static void main(String[] args) {
		
		int x = 24;
		int y = 2;
		y+=x;
		System.out.println(y);
		
		int l=28;
		int m=6;
		l-=m;
		System.out.println(l);
		
		int a=12;
		int b=10;
		b*=a;
		System.out.println(b);
		
		int r=60;
		int s=12;
		r/=s;
		System.out.println(r);

	}

}
